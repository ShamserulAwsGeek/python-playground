# Float variables
num1 = 5
num2 = 3.5

# Basic arithmetic operations
result1 = num1 + num2
print(result1)

result2 = num1 - num2
print(result2)

result3 = num1 * num2
print(result3)

result4 = num1 / num2
print(result4)

result5 = num1 % num2
print(result5)

# rounding 
result6 = round(3.14159265359, 2) # round to 2 decimal places
print("rounded value:", result6)
